#pragma config(Sensor, dgtl2,  univStop,       sensorTouch)
#pragma config(Sensor, dgtl3,  startLimit,     sensorTouch)
#pragma config(Sensor, dgtl4,  endLimit,       sensorTouch)
#pragma config(Sensor, dgtl5,  button,         sensorTouch)
#pragma config(Sensor, dgtl6,  button,         sensorNone)
#pragma config(Motor,  port1,           light,         tmotorVexFlashlight, openLoop, reversed)
#pragma config(Motor,  port3,           driveStart,    tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port4,           driveEnd,      tmotorVex393_MC29, openLoop)

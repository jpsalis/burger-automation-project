#include "conveyor_includes.c"
#include "e_stop.c"

task main()
{
	startTask(e_stop);
	bool last_DL_val, DL_val = false;
	int count = 0;

	while(true)
	{

		motor[light] = 0; // light off


		while(SensorValue[startLimit] == 0)
		{
			setConvSpeed(-50);
			delay(10);
		}

		setConvSpeed(0);
		motor[light] = 127; // light on

		while(SensorValue[endLimit] == 0)
		{
			DL_val = (bool)SensorValue[dataLine];

			if(DL_val && DL_val != last_DL_val) // rising edge detector
			{
				if(count == 0)
					clearTimer(T1);

				count++;
			}

			if(count != 0 && time1[T1] > 100)
			{
				if(count == 1) // if received 1 pulse, start
					setConvSpeed(50);
				else if(count >= 2) // if received 2 or more pulses, stop
					setConvSpeed(0);

				count = 0;
			}

			last_DL_val = DL_val;
		}

		setConvSpeed(0);
		while(SensorValue[button] == 0)
			delay(10);
	}

}
